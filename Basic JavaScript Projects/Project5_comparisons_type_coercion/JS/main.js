// Triple Equal Signs Assignment
// === compares both VALUE and DATA TYPE

// ✔ Return true: same value + same data type
console.log(10 === 10);  // true

// ✔ Return false: different value + different data type
console.log(5 === "20");  // false

// ✔ Return false: same value + different data type
console.log(7 === "7");  // false

// ✔ Return false: same data type + different value
console.log(15 === 10);  // false
