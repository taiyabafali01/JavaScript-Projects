function multipleOperations() {
    var addition = 5 + 3;
    var subtraction = 10 - 4;
    var multiplication = 6 * 2;
    var division = 20 / 5;

    document.getElementById("Math").innerHTML = 
        "Addition: " + addition + "<br>" +
        "Subtraction: " + subtraction + "<br>" +
        "Multiplication: " + multiplication + "<br>" +
        "Division: " + division;
}
